import numpy as np

a = np.array([1, 2, 3])
b = np.array([[1.3, 3.1, 5.6], [2.7, 4.9, 6.2]])

# ====== Выведем наши массивы =====
print("Массивы:")
print(a)
print(b)
print()

# ===== Определим размерность массивов =====
print("Размерность массивов")
print(f" a: {a.ndim}")
print(f" b: {b.ndim}")
print()

# ===== Определим форму массвов ======
print("Форма массивов")
print(f" a: {a.shape[0]}")
print(f" b: {b.shape[0]} * {b.shape[1]}")
print()

# ===== Определим тип данных и количество элементов в массиве ======

print(f"Тип данных a: {a.dtype}, количество элементов: {a.size}")
print(f"Тип данных b: {b.dtype}, количество элементов: {b.size}")
print()

# ===== Индексация и срезы =====
print("Индексация и срезы:")
print(f"a[0]: {a[0]}")            # первый элемент
print(f"a[-1]: {a[-1]}")          # последний элемент
print(f"a[1:]: {a[1:]}")          # срез со второго до конца
print(f"b[0, 1]: {b[0, 1]}")      # строка 0, столбец 1
print(f"b[:, 0]: {b[:, 0]}")      # весь первый столбец
print(f"b[1, :]: {b[1, :]}")      # вся вторая строка
